def greet(name):
    """
    Функция приветствия
    """
    return f"Привет, {name}!"

def add(a, b):
    """
    Функция сложения двух чисел
    """
    return a + b

if __name__ == "__main__":
    # Пример использования функции приветствия
    print(greet("Мир"))

    # Пример использования функции сложения
    print(add(5, 3))
